WITH MAX_COLONY AS (
    SELECT YEAR(DIFFERENTIATION_DATE) YEAR,
    ID,
    SIZE_OF_COLONY,
    MAX(SIZE_OF_COLONY) OVER(PARTITION BY YEAR(DIFFERENTIATION_DATE)) AS YEAR_DEV
    FROM ECOLI_DATA
)
SELECT YEAR, YEAR_DEV - SIZE_OF_COLONY YEAR_DEV, ID
FROM MAX_COLONY
ORDER BY YEAR, YEAR_DEV